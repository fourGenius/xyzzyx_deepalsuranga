    private void generate_lecture_id() {
        
        String id = "ID";
        String lc = "LE";

        employee_academic_user_info user_info = new employee_academic_user_info();
        String countid = user_info.getIdemployee_academic_user();
        int idcount = Integer.parseInt(countid);
        id_no = ++idcount;

        String a = Integer.toString(id_no);
        int length = a.length();
        System.out.println(length);
        
        String idn = Integer.toString(id_no);
        String zeros;
        if (length == 1) {
            zeros = "00000";
        } else if (length == 2) {
            zeros = "0000";
        } else if (length == 3) {
            zeros = "000";
        } else if (length == 4) {
            zeros = "00";
        } else if (length == 5) {
            zeros = "0";
        } else {
            zeros = "";
        }
        
        if (_rb_registration_lecture_information_form_colombo.isSelected()) {
            branch_name = "COL";
        } else {
            branch_name = "KAN";
        }

        lecture_id = id + "-" + lc + "-" +branch_name+"-"+ zeros + id_no;
        _lb_registration_lecture_preview_lectureID.setText(lecture_id);

    }

