    private void check() {
        boolean flag = false;
        JTextField temTf = null;
        for (JTextField tf : getTf()) {
            if (tf.getText().toString().trim().equals("")) {
                flag = true;
                temTf = tf;
                break;

            }
        }
        if (flag) {

            temTf.grabFocus();

        } else {

            try {
                ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select qty from inv_stock where itemid='" + id.getText() + "'");
                if (rs.next()) {
                    
//                   MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate("update inv_stock set  where itemid='" + id.getText() + "'");
                } else {
                   
                    MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate("insert into inv_stock (itemid,itemname,state) values('" + id.getText() + "','" + name.getText() + "','Active') ");
                }
                loadCombo();
                clear();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

    private JTextField[] getTf() {
        JTextField arr[] = {id, name};
        return arr;
    }

   

    private void clear() {
        name.setText(null);
       
        id.setText(null);
    }

    private void loadCombo() {
        try {
            ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select itemid from inv_stock where state='Active'");
            sid.removeAllItems();
            while (rs.next()) {
                sid.addItem(rs.getString("itemid"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
