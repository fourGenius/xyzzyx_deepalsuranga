    private void loadCombo() {
        try {
            ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select fname from inv_customer where state='Active' order by fname asc");

            sfname.removeAllItems();
            while (rs.next()) {

                sfname.addItem(rs.getString("fname"));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadId() {
        try {
            ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select count(id)as x from inv_customer");
            if (rs.next()) {
                int i = Integer.parseInt(rs.getString("x"));
                ++i;
                id.setText("CUS00" + i);
                ResultSet rs1 =MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select * from inv_customer where id='" + id.getText() + "'");
                if (rs1.next()) {
                    ++i;
                    id.setText("CUS00" + i);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void check() {
        boolean flag = false;
        JTextField temTf = null;
        for (JTextField tf : getTf()) {
            if (tf.getText().toString().trim().equals("")) {
                flag = true;
                temTf = tf;
                break;

            }
        }
        if (flag) {

            temTf.grabFocus();

        } else {
            try {
                String addre = address1.getText() + "," + address2.getText() + "," + address3.getText();
                MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate("insert into inv_customer (id,fname,lname,nic,address,addressl1,addressl2,addressl3,tp,fax,email,state) values('" + id.getText() + "','" + fname.getText() + "','" + lname.getText() + "','" + nic.getText() + "','" + addre + "','" + address1.getText() + "','" + address2.getText() + "','" + address3.getText() + "','" + tp.getText() + "','" + Fax.getText() + "','" + email.getText() + "','Active')");
                JOptionPane.showMessageDialog(this, "Successfully Save");
                loadCombo();
                clear();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

