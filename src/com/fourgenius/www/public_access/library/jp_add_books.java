    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

                // TODO add your handling code here:
        //get todays date
        Date date = new Date();
        DateFormat dateformat = new SimpleDateFormat("yyyy/MM/dd");
        
        String category = (String) jc_categorycombo.getSelectedItem();
        

        try {
            //check if fields are empty
            if (tf_name.getText().equals("") || tf_author.getText().equals("") || tf_isbn.getText().equals("") || tf_shelf.getText().equals("") || category.equals("String") ) {
                JOptionPane.showMessageDialog(this, "Please fill in all fields ", "", JOptionPane.WARNING_MESSAGE);
            } 
            //shelf number must be positive
            else if (Integer.parseInt(tf_shelf.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Invalid Shelf number has been entered", "", JOptionPane.WARNING_MESSAGE);
            }
            else {
                String title = tf_name.getText();
                String writer = tf_author.getText();
                String ISBN = tf_isbn.getText();
                int shelfno = Integer.parseInt(tf_shelf.getText());

               
                //auto-increment primary key
                int bookid=0;
                String query="SELECT LastID FROM lib_generateid WHERE raw='BID'";
                ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery(query);
                while(rs.next()) {
                    bookid = Integer.parseInt(rs.getString("LastID"));
                }
                //insert a record to book table
                String query1 = "INSERT INTO lib_book VALUES('B" + bookid + "','" + title + "','" + writer + "','" + category+ "','"+ISBN + "','" + shelfno + "','" + dateformat.format(date) + "','Available')";
                MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate(query1);
                JOptionPane.showMessageDialog(this,"New Book Details were added Successfully; Book ID = B"+bookid);
                //auto-increment primary key
                bookid=bookid+1;
                String qry="UPDATE lib_generateid SET LastID='"+bookid+"' WHERE raw='BID'";
                MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate(qry);
                
                reseting();
                MC_JavaDataBaseConnection.myConnection().close();
                
            }
        } 
        catch (NumberFormatException n){
                JOptionPane.showMessageDialog(this, " Only Enter Numbers to shelf  field!","",JOptionPane.ERROR_MESSAGE);
        }
        catch (Exception e){              
               JOptionPane.showMessageDialog(this, ""+e.getMessage()+" Error,  Book Not Added!","",JOptionPane.ERROR_MESSAGE);
        }   
        
    }//GEN-LAST:event_jButton1ActionPerformed


