    private void loadTable() {
        
        try {
            ResultSet rs=MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("SELECT * FROM `lib_member` WHERE `status`='Active'");
            DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
            
            while (rs.next()) {
               Vector v=new Vector();
                v.add(rs.getString("MID"));
                v.add(rs.getString("fName"));
                v.add(rs.getString("lName"));
                v.add(rs.getString("address"));
                v.add(rs.getString("telephone"));
                
                dtm.addRow(v);
            }
        } catch (Exception e) {
        }
   
        
    }
