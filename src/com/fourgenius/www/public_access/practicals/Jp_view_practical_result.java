    private void load_passed_table_data(String practical_id) {
        try {
            DefaultTableModel dtm=(DefaultTableModel)_tb_view_marks_passed_student.getModel();
            
            Connection c=MC_JavaDataBaseConnection.myConnection();
            Statement s=c.createStatement();
            String search_query="SELECT a.stu_user_info_id, a.stu_practical_mark, a.stu_practical_result, b.stu_info_name_first_name, b.stu_info_name_last_name FROM stu_practical_result a LEFT JOIN stu_info_name b ON a.stu_user_info_id=b.stu_user_info_id WHERE a.stu_practical_info_id='"+practical_id+"' AND a.stu_practical_result='Pass'";
            ResultSet rs= s.executeQuery(search_query);
            while (rs.next()) {                
                Vector v=new Vector();
                v.add(rs.getString("stu_user_info_id"));
                v.add(rs.getString("stu_info_name_first_name")+" "+rs.getString("stu_info_name_last_name"));
                v.add(rs.getString("stu_practical_mark"));
                v.add(rs.getString("stu_practical_result"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

